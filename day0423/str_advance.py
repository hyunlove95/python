language = "python is a great language"

# find
a = language.find("great")
print("a :", a)

# replace
b = language.replace("python", "java")
print("b :", b)

# upper, lower
c = language.upper()
print("c :", c)

# split[찢다]
d = language.split(" ")
print("d :", d)

# join
e = "!!".join(["hello", " world"])
print("e :", e)